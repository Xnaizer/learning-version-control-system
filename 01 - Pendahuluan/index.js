// Git adalah sistem kontrol versi (Version Control System, VCS) yang digunakan untuk melacak perubahan pada kode atau file selama proses pengembangan. Git membantu tim pengembang berkolaborasi, mengelola versi yang berbeda dari sebuah proyek, dan melacak riwayat perubahan kode secara efisien. 

// Linus Torvalds mulai dikembangkan 2005 april, membantu mengontrol source code kernel linux.

// Kenapa Git Penting?
// Git menjadi standar industri dalam pengembangan perangkat lunak karena:

// Membantu tim mengelola proyek dengan baik.
// Mencegah hilangnya data atau konflik kode.
// Memfasilitasi pengembangan fitur atau perbaikan bug secara paralel.

// Latar Belakang
// Awal Penggunaan BitKeeper:

// Sebelum Git, pengembangan kernel Linux menggunakan sistem kontrol versi bernama BitKeeper. BitKeeper adalah sistem kontrol versi terdistribusi yang cukup canggih pada saat itu.
// Namun, BitKeeper bukan perangkat lunak open-source. Pengembang kernel Linux diberikan lisensi gratis dengan syarat tertentu.
// Masalah dengan BitKeeper:

// Pada tahun 2005, hubungan antara pengembang Linux dan BitMover (perusahaan pemilik BitKeeper) memburuk. Hal ini menyebabkan pencabutan lisensi gratis bagi pengembang kernel Linux.
// Karena kebutuhan mendesak, Linus Torvalds, pencipta Linux, memutuskan untuk membuat sistem kontrol versi baru yang:
// Terdistribusi seperti BitKeeper.
// Cepat dan efisien.
// Open-source dan bebas digunakan.
// Pembuatan Git
// Maret 2005:

// Linus Torvalds mulai mengembangkan Git dengan fokus pada kinerja tinggi, dukungan branching dan merging yang kuat, serta desain terdistribusi.
// Tujuan utama adalah menciptakan alat yang sesuai untuk pengembangan kernel Linux.
// April 2005:

// Versi pertama Git dirilis hanya dalam waktu 10 hari setelah proyek dimulai.
// Nama "Git" dipilih oleh Linus Torvalds. Menurutnya, "Git" adalah istilah slang Inggris untuk "orang bodoh" atau "orang menyebalkan," sebagai candaan tentang dirinya sendiri.
// Filosofi Desain Git
// Kecepatan:

// Sistem ini dirancang untuk menangani pengembangan proyek besar dengan cepat.
// Keamanan:

// Git menggunakan algoritma hashing SHA-1 untuk memastikan integritas data. Setiap file dan commit diberi identifikasi unik berdasarkan isinya.
// Distribusi:

// Setiap pengembang memiliki salinan lengkap dari seluruh repositori, termasuk sejarah perubahan.
// Branching dan Merging:

// Branching di Git dirancang ringan dan fleksibel, sehingga pengembang dapat dengan mudah membuat branch untuk eksperimen.
// Evolusi Git
// 2005â€“2006:

// Git mulai digunakan secara luas dalam pengembangan kernel Linux.
// Dokumentasi dan komunitas Git mulai berkembang.
// 2007:

// Proyek hosting seperti GitHub diluncurkan, menjadikan Git lebih populer di kalangan pengembang di luar kernel Linux.
// 2010-an:

// Git menjadi standar industri untuk sistem kontrol versi, menggantikan alat lain seperti Subversion (SVN) dan CVS.
// Banyak perusahaan besar mulai mengadopsi Git untuk pengembangan perangkat lunak mereka.
// Fakta Menarik
// Linus Torvalds hanya mengembangkan Git selama beberapa bulan pertama. Setelah itu, pengembangan Git diteruskan oleh komunitas open-source.
// Saat ini, Git dikelola oleh komunitas global, dengan kontribusi dari ribuan pengembang.
// Git Hari Ini
// Git adalah sistem kontrol versi paling populer di dunia.
// Banyak layanan dan platform yang mendukung Git, seperti GitHub, GitLab, Bitbucket, dan lainnya.
// Digunakan oleh hampir semua jenis pengembang, mulai dari individu hingga tim besar di perusahaan teknologi seperti Google, Facebook, dan Microsoft.
// Git telah menjadi fondasi penting dalam pengembangan perangkat lunak modern, membantu pengembang bekerja secara kolaboratif dan menjaga kualitas kode.


